#ifndef TOKEN_H
#define TOKEN_H

#include <string>

enum TokenType {
	// Separator/punctuator
	// LEFT_PAREN, RIGHT_PAREN,
	LEFT_BRACE, RIGHT_BRACE,
	COLON,
	SEMICOLON,

	// Operators
	PLUS, MINUS, STAR, SLASH,
	AND, OR,

	BANG, BANG_EQUAL,
	EQUAL,
	GREATER, GREATER_EQUAL,
	LESS, LESS_EQUAL,

	// literals
	NUMBER, STRING,

	IDENTIFIER,

	// keywords
	IF, ELSE,
	REPEAT, DO, WHILE,
	DEF,
	PRINT, CHAR_PRINT, PEEK, CHAR_PEEK,
	LN, PRINT_LN, CHAR_PRINT_LN,
	DROP,
	DUP,
	SWAP, N_SWAP,
	INPUT, CHAR_INPUT,
	DUMP,

	// comments
	// HASHTAG,

	// for parser
	END
};

// a data class for tokens generated by the scanner.
struct Token {
	TokenType type;
	std::string lexeme;
	int literal;
	int line;
};

#endif
